)

@OptIn(ExperimentalMaterial3ExpressiveApi::class)
@Composable
fun AppTheme(
    themePreference: String = ThemeKeys.SYSTEM, // Added themePreference parameter
    // Dynamic color is available on Android 12+
    dynamicColor: Boolean = true, // Kept dynamicColor, can be true if needed
    content: @Composable() () -> Unit
) {
    val useDarkTheme = when (themePreference) {
        ThemeKeys.LIGHT -> false
        ThemeKeys.DARK -> true
        else -> isSystemInDarkTheme() // Default to system for ThemeKeys.SYSTEM or any other value
    }

    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (useDarkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        useDarkTheme -> darkScheme
        else -> lightScheme
    }

    CompositionLocalProvider(LocalAppUseDarkTheme provides useDarkTheme) {
        val view = LocalView.current
        if (!view.isInEditMode) {
            SideEffect {
                val window = (view.context as Activity).window
                window.statusBarColor = colorScheme.primary.toArgb() // You might want to use a specific status bar color
                WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = !useDarkTheme
            }
        }

        MaterialExpressiveTheme(
            colorScheme = colorScheme,
            typography = AppTypography, // Assuming AppTypography is defined elsewhere
            content = content,
            shapes = Shapes(),
            motionScheme = MotionScheme.expressive()
        )
    }
}
